package NumberSystem;

public class N3 {
    public static void main(String[] args) {
        int x = 061;    //octal
        int y = 61;     //decimal 
        int z = 0x61;   // hexadecimal

        System.out.println(" x = "+ x);
        System.out.println(" y = "+ y);
        System.out.println(" z = "+ z);
    }
    
}
