package Assingment3.Array;
// print all element  pair set in given array;
public class A3 {
public static void main(String[] args) {
    int  arr[] = { 10,20,30,};
    int n  = 3; 
    for( int i=0; i<n; i++){
        for( int j = 0; j<n ; j++ ){
    
            System.out.println( arr[i]+", "+ arr[j] );
    }
    } System.out.println();

}
}
